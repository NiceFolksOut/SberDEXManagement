import Foundation
import UIKit

extension UIColor {
    class var algaeGreen: UIColor {
        return UIColor(red: 39.0 / 255.0, green: 209.0 / 255.0, blue: 112.0 / 255.0, alpha: 1.0)
    }
    class var warmGrey: UIColor {
        return UIColor(white: 139.0 / 255.0, alpha: 1.0)
    }
    class var greyishBrown: UIColor {
        return UIColor(white: 74.0 / 255.0, alpha: 1.0)
    }
    class var sbrWhite: UIColor {
        return UIColor(white: 237.0 / 255.0, alpha: 1.0)
    }
}

